package entity;

import java.awt.image.BufferedImage;

public class Entity {
    public int x, y;
    public BufferedImage sprite;

    public int getX(){
        return x;
    }

    public int getY(){
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setPosition(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
